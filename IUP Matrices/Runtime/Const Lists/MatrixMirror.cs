namespace IUP.Toolkits.Matrices
{
    /// <summary>
    /// Перечисление, представляющее тип отзеркаливания матрицы.
    /// </summary>
    public enum MatrixMirror : byte
    {
        /// <summary>
        /// Горизонтальное отражение матрицы.
        /// </summary>
        Horizontal = 0,
        /// <summary>
        /// Вертикальное отражение матрицы.
        /// </summary>
        Vertical = 1
    }
}
